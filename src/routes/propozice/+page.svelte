<h3>Popis závodu</h3>

Trailový terénní značený závod jednotlivců v okolí slapské přehrady. V rámci závodu budou dva měřené rychlostní úseky. V mapě, kterou obdrží závodníci na prezentaci bude označen start a cíl těchto úseků. Měřené úseky jsou hodnoceny zvlášť mimo celkové pořadí.

